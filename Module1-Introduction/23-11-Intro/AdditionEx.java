package com.a2311;

public class AdditionEx 
{
	public static void main(String[] args) 
	{
		
		int a = 6;
		int b = 5;
		
		int add = a+b;
		System.out.println("\n Your Addition is :" + add);
		
		int sub = a-b;
		System.out.println("\n Your Substraction is :" +sub);
		
		int mul = a*b;
		System.out.println("\n Your Multiplication is :" + mul);
		
		int div= a/b;
		System.out.println("\n Your Division is :" + div);
		
		int rem = a%b;
		System.out.println("\n Your Modulus is :" + rem);
		
	}
}
