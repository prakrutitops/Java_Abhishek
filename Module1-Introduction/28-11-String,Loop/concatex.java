package com.a2811;

public class concatex 
{
	public static void main(String[] args) 
	{
		
		String a ="sachin";
		String b ="tendulkar";
	
		//1
		System.out.println(a+" "+b);
		
		//2 //immutable strings
		String c = a.concat(b);
		System.out.println(c);
	
	
	
		/*
		 	
		 	Immutable - can not change
		 	Mutable - can change
		 	
		 */
	
	}
}
